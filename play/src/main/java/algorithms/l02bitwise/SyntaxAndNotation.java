package algorithms.l02bitwise;

public class SyntaxAndNotation {

  public static void main(String[] args) {
    byte x1 = 127;        // decimal 127
    byte x2 = 100;        // decimal 100
    byte x3 = 00000100;   // octal. decimal 64
    byte x4 = 0b00000100; // binary. decimal 8
    int  x5 = 0x10;       // hexadecimal. decimal 16
  }

}
